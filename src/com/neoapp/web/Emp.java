// Emp.java (Bean class)
package com.neoapp.web;

public class Emp {
	private String no;
	private String name;
	private float salary;

	public String getNo() {
		return no;
	}

	public String getName() {
		return name;
	}

	public float getSalary() {
		return salary;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setSalary(float salary) {
		this.salary = salary;
	}
}
